                    $(".menu").css({"box-shadow":"rgba(0, 0, 0, 0.31) 6px 1px 1px"});
                    $(".menu").css({"z-index":"100"});
                }else{
                    //cuando no hay scroll
                    $(".menu").css({"position":"relative"});
                    $(".menu").css({"background":"transparent"});
                    $(".menu").css({"box-shadow":"0 0 0"});
